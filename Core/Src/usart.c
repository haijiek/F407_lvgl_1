//******************  初始化串口IO  ***************
void USART3_Init_IO(void)
{
  memset(&usart3_rx_handler, 0, sizeof(USART3_RxHandler_t));
  memset(&usart3_tx_handler, 0, sizeof(USART3_TxHandler_t));

  __HAL_UART_ENABLE_IT(&huart3, UART_IT_IDLE);
  HAL_UARTEx_ReceiveToIdle_DMA(&huart3, usart3_rx_handler.rx_buf, USART3_RX_BUF_SIZE);
}

//******************  发送命令  ***************
void Send_AT_Command(const char *cmd)
{
  uint16_t len = strlen(cmd);

  memcpy(usart3_tx_handler.tx_buf, cmd, len);
  usart3_tx_handler.tx_len = len;

  printf("%s\r\n", cmd);
}

